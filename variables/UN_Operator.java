package variables;

public class UN_Operator {

	public static void main(String[] args) {

	int x =10;
	System.out.println(x++);//10  post increment  (11)
	System.out.println(++x);//12   pre incremet
	System.out.println(x--);//12  post decrement   (11)
	System.out.println(--x);//10   pre decrement

		}

}
